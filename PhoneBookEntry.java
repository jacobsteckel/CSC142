public class PhoneBookEntry
{
   String name;
   int phoneNumber;
   
   public PhoneBookEntry()
   {
   
   }
      
   public String getName()
   {
      return name;
   }
   public int getPhoneNumber()
   {
      return phoneNumber;
   }
   
   public void setName(String n)
   {
      name=n;
   }
   public void setPhoneNumber(int pn)
   {
      phoneNumber=pn;
   }         
}